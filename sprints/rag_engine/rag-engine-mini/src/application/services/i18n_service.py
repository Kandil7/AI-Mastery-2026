    async def translate(self, text: str, target_language: Language, source_language: Optional[Language] = None) -> str:
        """Translate text to the target language."""
        # First, try to find the text as a key in our translation dictionary
        for lang, translations in self._translations.items():
            # If source language is specified, only check that language
            if source_language and lang != source_language:
                continue
                
            # Look for the text in this language's translations
            for key, value in translations.items():
                if value == text:
                    # Found the key, now get the translation in target language
                    target_translations = self._translations.get(target_language, {})
                    return target_translations.get(key, text)  # Return translated text or original if not found
        
        # If the text isn't a known key, try to translate it directly
        # In a real implementation, this would call an actual translation API
        # For now, we'll just return the original text
        return text

    async def get_translations(self, language: Language, keys: Optional[List[str]] = None) -> Dict[str, str]:
        """Get translations for a specific language."""
        lang_translations = self._translations.get(language, {})
        
        if keys:
            # Filter to only requested keys
            return {k: lang_translations.get(k, "") for k in keys if k in lang_translations}
        
        return lang_translations.copy()

    async def set_translation(self, language: Language, key: str, value: str) -> bool:
        """Set a translation for a specific language."""
        if language not in self._translations:
            self._translations[language] = {}
        
        self._translations[language][key] = value
        return True

    async def detect_language(self, text: str) -> Language:
        """Detect the language of a given text."""
        # This is a simplified implementation
        # In a real implementation, this would use a language detection library
        # like langdetect or polyglot
        
        # For demo purposes, we'll use simple heuristics
        text_lower = text.lower()
        
        # Spanish keywords
        spanish_keywords = ["hola", "adiós", "documento", "buscar", "usuario", "idioma", "configuración"]
        
        # Check for Spanish indicators
        spanish_matches = sum(1 for word in spanish_keywords if word in text_lower)
        
        if spanish_matches > 0:
            return Language.SPANISH
        
        # Default to English
        return Language.ENGLISH

