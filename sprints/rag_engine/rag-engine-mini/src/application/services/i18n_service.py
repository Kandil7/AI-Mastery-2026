class Language(str, Enum):
    """Supported languages in the system."""
    ENGLISH = "en"
    SPANISH = "es"
    CHINESE = "zh"
    ARABIC = "ar"
    FRENCH = "fr"
    GERMAN = "de"
    JAPANESE = "ja"
    KOREAN = "ko"
    PORTUGUESE = "pt"
    RUSSIAN = "ru"


class I18nServicePort(ABC):
    """Abstract port for internationalization services."""

    @abstractmethod
    async def translate(self, text: str, target_language: Language, source_language: Optional[Language] = None) -> str:
        """Translate text to the target language."""
        pass

    @abstractmethod
    async def get_translations(self, language: Language, keys: Optional[List[str]] = None) -> Dict[str, str]:
        """Get translations for a specific language."""
        pass

    @abstractmethod
    async def set_translation(self, language: Language, key: str, value: str) -> bool:
        """Set a translation for a specific language."""
        pass

    @abstractmethod
    async def detect_language(self, text: str) -> Language:
        """Detect the language of a given text."""
        pass

    @abstractmethod
    async def get_available_languages(self) -> List[Language]:
        """Get a list of all available languages."""
        pass


